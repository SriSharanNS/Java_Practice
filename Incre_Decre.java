public class Incre_Decre {
    public static void main(String[] args) {
        

        /* Increment Operator */

        int a=10;
        System.out.println(a);
        System.out.println(++a);
        System.out.println(a);

        int b=10;
        System.out.println(b);
        System.out.println(b++);
        System.out.println(b);

        /* Decrement Operator */

        int c=10;
        System.out.println(c);
        System.out.println(--c);
        System.out.println(c);

        int d=10;
        System.out.println(d);
        System.out.println(d--);
        System.out.println(d);




    }
}
