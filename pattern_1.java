public class pattern_1 {
    public static void main(String[] args) {
        
        int r=3,c=3;
        int i=1;
        while(i<=r)
        {
            int j=1;

            while (j<=c)
            {
                System.out.print('*');
                j++;
            }

            System.out.println();
            i++;

        }

    }
}
