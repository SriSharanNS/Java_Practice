public class Widening {
    public static void main(String[] args) {
         

        /* Widening */

        int a =10;
        long b =  a;

        System.out.println(a);
        System.out.println(b);

        float c =20;
        double d =  c;

        System.out.println(c);
        System.out.println(d);


    }
}
