public class p3 {
    public static void main(String[] args) {
        

        System.out.println("For Integer values");

        int a =10,b=20;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);

       System.out.println("For Floating values");

        float c =10f,d=20f;
        
        System.out.println(c+d);
        System.out.println(c-d);
        System.out.println(c*d);
        System.out.println(c/d);
        System.out.println(c%d);


    }
}
